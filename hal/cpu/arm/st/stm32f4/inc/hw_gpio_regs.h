//*****************************************************************************
//
//! \addtogroup STM32F4xx_GPIO_Register_Offsets GPIO Register Offset(Map)
//! \brief Here is the GPIO register offset, users can get the register address
//! through <b>GPIO_BASE + offset</b>.
//! @{
//
//*****************************************************************************

//
//! Port Mode Register
//
#define GPIO_MODER                0x00000000

//
//! Port Output Type Register
//
#define GPIO_OTYPER               0x00000004

//
//! Port Output Speed Register
//
#define GPIO_OSPEEDR              0x00000008
//
//! Port Pull Up Pull Down Register
//
#define GPIO_PUPDR				  0x0000000C

//
//! Port input data register
//
#define GPIO_IDR                0x00000010

//
//! Port output data register
//
#define GPIO_ODR                0x00000014

//
//! Port bit set/reset register
//
#define GPIO_BSRR               0x00000018

//
//! Port bit reset register
//
#define GPIO_BRR                0x00000018

//
//! Port configuration lock register
//
#define GPIO_LCKR               0x0000001C
